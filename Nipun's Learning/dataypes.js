let numvar = 324;
console.log(`${numvar} is of ${typeof(numvar)} type`);

let numvar2 = 324.56;
console.log(`${numvar2} is also of ${typeof(numvar2)} type`);

let str = "Anything within single quotes or double quotes";
console.log(`${str} is of ${typeof(str)} type`);

let bool = true;
console.log(`${bool} and ${!bool} are ${typeof(bool)} type`);

let u;
console.log(`Un-initialized variables are of ${typeof(u)} type`)

let n = null;
console.log(`${n} is a special datatype and it is of ${typeof(n)} type`)