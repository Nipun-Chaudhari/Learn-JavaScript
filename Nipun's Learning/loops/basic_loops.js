// the for loop

for (let index = 0; index < 11; index++) {
    // const element = array[index];
    console.log(index)
}

// the while loop

let jump = 0
while (jump <= 20) {
    console.log(jump)
    jump += 2
}

// the do while loop

let start = 3
do {
    console.log(start)
    start++
} while (start <= 10);

// in such cases loop runs at least one time

let started = 22
do {
    console.log(start)
    started++
} while (started <= 10);